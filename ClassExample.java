// Program: Example of a Class in Java

// Defining a simple class
class Student {
    String name;
    int age;

    void display() {
        System.out.println("Name: " + name + ", Age: " + age);
    }
}

public class ClassExample {
    public static void main(String[] args) {
        System.out.println("This program defines a Student class.");
    }
}


 /* 
Output:
This program defines a Student class.
 */